#!/usr/bin/env python3
"""
Surgical migration splitter for core/migrations/0004_*.py

Splits 871-line migration with 15 models into 15 separate migrations.
Each migration file will be ≤200 lines per SARAISE standards.

Usage:
    python scripts/split_migration_0004.py
"""
import os
import re
from pathlib import Path

# Paths
BACKEND_DIR = Path(__file__).parent.parent
MIGRATIONS_DIR = BACKEND_DIR / "src/core/migrations"
ORIGINAL_FILE = MIGRATIONS_DIR / "0004_compliancecheck_entitlementcheck_guardrailrule_and_more.py"

# Models to extract (in order)
MODELS = [
    "ComplianceCheck",
    "EntitlementCheck",
    "GuardrailRule",
    "GuardrailViolation",
    "ModuleRegistryEntry",
    "ModuleInstallation",
    "ModuleUpgrade",
    "PolicyBundleValidation",
    "ResidencyRule",
    "SubscriptionPlan",
    "PlanEntitlement",
    "TenantModuleInstallation",
    "TenantSubscription",
    "UpgradeStep",
    "InstallationStep",
]


def read_original_migration():
    """Read the original migration file."""
    with open(ORIGINAL_FILE, 'r') as f:
        return f.read()


def extract_imports(content):
    """Extract import statements from migration."""
    lines = content.split('\n')
    imports = []
    for line in lines:
        if line.startswith('import ') or line.startswith('from '):
            imports.append(line)
        elif line.startswith('class Migration'):
            break
    return '\n'.join(imports)


def extract_model_operation(content, model_name):
    """Extract a single CreateModel operation for a model."""
    # Find the start of this model's CreateModel
    pattern = rf'migrations\.CreateModel\(\s*name="{model_name}"'
    match = re.search(pattern, content)
    if not match:
        return None

    start_pos = match.start()

    # Find the end of this CreateModel (next CreateModel or end of operations)
    rest_content = content[start_pos:]

    # Count parentheses to find the end
    paren_count = 0
    in_create_model = False
    end_pos = start_pos

    for i, char in enumerate(rest_content):
        if char == '(':
            paren_count += 1
            in_create_model = True
        elif char == ')':
            paren_count -= 1
            if in_create_model and paren_count == 0:
                end_pos = start_pos + i + 1
                break

    operation = content[start_pos:end_pos]

    # Add proper indentation (8 spaces for operations list)
    lines = operation.split('\n')
    indented_lines = []
    for line in lines:
        if line.strip():
            indented_lines.append(' ' * 8 + line.strip())
        else:
            indented_lines.append('')

    return '\n'.join(indented_lines) + ','


def create_migration_file(model_name, operation, migration_number, dependency_number):
    """Create a new migration file for a single model."""
    # Convert model name to snake_case for file name
    file_name = re.sub(r'(?<!^)(?=[A-Z])', '_', model_name).lower()
    migration_file = f"{migration_number:04d}_create_{file_name}.py"

    # Get imports (simplified - only what's needed)
    imports = "from django.db import migrations, models"

    # Check if we need special imports
    if "compliance" in file_name:
        imports += "\nimport src.core.compliance_models"
    elif "entitlement" in file_name:
        imports += "\nimport src.core.entitlement_models"
    elif "guardrail" in file_name:
        imports += "\nimport src.core.module_guardrail_models"
    elif "installation" in file_name or "upgrade" in file_name:
        imports += "\nimport src.core.module_installation_models"
        imports += "\nimport src.core.module_upgrade_models"
    elif "registry" in file_name:
        imports += "\nimport src.core.module_registry_models"
    elif "subscription" in file_name or "plan" in file_name:
        imports += "\nimport src.core.subscription_models"
    elif "residency" in file_name:
        imports += "\nimport src.core.residency_models"
    elif "policy" in file_name:
        imports += "\nimport src.core.policy_models"

    # Handle foreign key imports
    if "django.db.models.deletion" in operation:
        imports += "\nimport django.db.models.deletion"

    # Create migration content
    content = f'''# Generated by Django 5.0.6 on 2026-01-09 (split from 0004)

{imports}


class Migration(migrations.Migration):

    dependencies = [
        ("core", "{dependency_number:04d}_{'initial' if dependency_number == 3 else 'create_' + prev_model_name(model_name)}"),
    ]

    operations = [
{operation}
    ]
'''

    return migration_file, content


def prev_model_name(current_model):
    """Get the previous model name in snake_case."""
    idx = MODELS.index(current_model)
    if idx == 0:
        return "initial"
    prev = MODELS[idx - 1]
    return re.sub(r'(?<!^)(?=[A-Z])', '_', prev).lower()


def main():
    print("🔪 Surgical Migration Splitter")
    print("=" * 50)
    print(f"Original file: {ORIGINAL_FILE}")
    print(f"Models to split: {len(MODELS)}")
    print()

    # Read original migration
    print("📖 Reading original migration...")
    content = read_original_migration()
    print(f"   Original size: {len(content)} chars, {len(content.splitlines())} lines")

    # Extract imports
    imports = extract_imports(content)

    # Process each model
    print("\n✂️  Splitting migrations...")
    created_files = []

    for i, model_name in enumerate(MODELS):
        migration_number = 4 + i  # Start from 0004
        dependency_number = 3 + i  # Dependency on previous migration

        print(f"\n   [{i+1}/{len(MODELS)}] Processing {model_name}...")

        # Extract operation
        operation = extract_model_operation(content, model_name)
        if not operation:
            print(f"      ❌ Could not extract operation for {model_name}")
            continue

        # Create migration file
        file_name, file_content = create_migration_file(
            model_name, operation, migration_number, dependency_number
        )

        # Calculate output path
        output_path = MIGRATIONS_DIR / file_name

        # Write file
        with open(output_path, 'w') as f:
            f.write(file_content)

        lines = len(file_content.splitlines())
        print(f"      ✅ Created: {file_name} ({lines} lines)")
        created_files.append((file_name, lines))

    print("\n" + "=" * 50)
    print("📊 Summary:")
    print(f"   Total migrations created: {len(created_files)}")
    print(f"   Average lines per migration: {sum(l for _, l in created_files) / len(created_files):.1f}")
    print(f"   Max lines: {max(l for _, l in created_files)}")
    print(f"   All files ≤200 lines: {'✅ YES' if all(l <= 200 for _, l in created_files) else '❌ NO'}")

    print("\n✅ Migration splitting complete!")
    print("\n⚠️  IMPORTANT: Do NOT delete the original 0004 file yet.")
    print("   First verify the split migrations work correctly.")
    print("\n📝 Next steps:")
    print("   1. Review generated migrations")
    print("   2. Test migrations: python manage.py migrate --fake-initial")
    print("   3. If successful, archive original 0004 file")


if __name__ == "__main__":
    main()
