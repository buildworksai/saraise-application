#!/usr/bin/env python3
"""
Fix broken tenant_management AddIndex migration.
"""
from pathlib import Path

BACKEND_DIR = Path(__file__).parent.parent
MIGRATIONS_DIR = BACKEND_DIR / "src/modules/tenant_management/migrations"
BACKUP_FILE = BACKEND_DIR / "migration-backups-2026-01-09/tenant_mgmt_0001_initial_BACKUP.py"

# Read the backup file
with open(BACKUP_FILE, 'r') as f:
    content = f.read()

# Find all AddIndex operations manually
lines = content.split('\n')
add_index_ops = []
current_op = []
in_add_index = False
paren_count = 0

for line in lines:
    if 'migrations.AddIndex(' in line:
        in_add_index = True
        paren_count = line.count('(') - line.count(')')
        current_op = [line]
    elif in_add_index:
        current_op.append(line)
        paren_count += line.count('(') - line.count(')')
        if paren_count <= 0 and '),' in line:
            # Found the end
            add_index_ops.append('\n'.join(current_op))
            current_op = []
            in_add_index = False
            paren_count = 0

print(f"Found {len(add_index_ops)} AddIndex operations")

# Indent operations properly
indented_ops = []
for op in add_index_ops:
    op_lines = op.split('\n')
    indented_lines = []
    for op_line in op_lines:
        stripped = op_line.strip()
        if stripped:
            indented_lines.append(' ' * 8 + stripped)
    indented_ops.append('\n'.join(indented_lines))

operations_str = '\n'.join(indented_ops)

content = f'''# Generated by Django 5.0.6 on 2026-01-09 (split from 0001)

from django.db import migrations, models


class Migration(migrations.Migration):

    dependencies = [
        ("tenant_management", "0005_create_tenant_health_score"),
    ]

    operations = [
{operations_str}
    ]
'''

output_path = MIGRATIONS_DIR / "0006_add_indexes.py"
with open(output_path, 'w') as f:
    f.write(content)

print(f"✅ Created 0006_add_indexes.py ({len(add_index_ops)} indexes, {len(content.splitlines())} lines)")
