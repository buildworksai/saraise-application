#!/usr/bin/env python3
"""
Fix broken AddIndex migrations by properly extracting them from the backup.
"""
from pathlib import Path

BACKEND_DIR = Path(__file__).parent.parent
MIGRATIONS_DIR = BACKEND_DIR / "src/modules/ai_agent_management/migrations"
BACKUP_FILE = BACKEND_DIR / "migration-backups-2026-01-09/ai_agent_0001_initial_BACKUP.py"

# Read the backup file
with open(BACKUP_FILE, 'r') as f:
    content = f.read()

# Find all AddIndex operations manually
lines = content.split('\n')
add_index_ops = []
current_op = []
in_add_index = False
paren_count = 0

for line in lines:
    if 'migrations.AddIndex(' in line:
        in_add_index = True
        paren_count = line.count('(') - line.count(')')
        current_op = [line]
    elif in_add_index:
        current_op.append(line)
        paren_count += line.count('(') - line.count(')')
        if paren_count <= 0 and '),' in line:
            # Found the end
            add_index_ops.append('\n'.join(current_op))
            current_op = []
            in_add_index = False
            paren_count = 0

print(f"Found {len(add_index_ops)} AddIndex operations")

# Split into batches of 15
batch_size = 15
batches = [add_index_ops[i:i + batch_size] for i in range(0, len(add_index_ops), batch_size)]

print(f"Split into {len(batches)} batches")

# Create migration files
prev_migration = "0021_create_shard_saturation"

for batch_num, batch in enumerate(batches, start=1):
    migration_num = 21 + batch_num
    file_name = f"{migration_num:04d}_add_indexes_batch_{batch_num}.py"

    # Indent operations properly
    indented_ops = []
    for op in batch:
        # Each operation should start with 8 spaces
        op_lines = op.split('\n')
        indented_lines = []
        for op_line in op_lines:
            # Remove leading whitespace and add 8 spaces
            stripped = op_line.strip()
            if stripped:
                indented_lines.append(' ' * 8 + stripped)
        indented_ops.append('\n'.join(indented_lines))

    operations_str = '\n'.join(indented_ops)

    content = f'''# Generated by Django 4.2.27 on 2026-01-09 (split from 0001)

from django.db import migrations, models


class Migration(migrations.Migration):

    dependencies = [
        ("ai_agent_management", "{prev_migration}"),
    ]

    operations = [
{operations_str}
    ]
'''

    output_path = MIGRATIONS_DIR / file_name
    with open(output_path, 'w') as f:
        f.write(content)

    print(f"✅ Created {file_name} ({len(batch)} indexes, {len(content.splitlines())} lines)")
    prev_migration = f"{migration_num:04d}_add_indexes_batch_{batch_num}"

print("\n✅ All index migrations created!")
