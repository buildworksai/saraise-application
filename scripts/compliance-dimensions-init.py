def create_dimension_file(dimension: dict):
    """Create compliance dimension YAML file."""
    dimensions_dir = GCR_ROOT / "dimensions"
    dimensions_dir.mkdir(parents=True, exist_ok=True)
    
    dimension_file = dimensions_dir / f"{dimension['id']}.yaml"
    
    yaml_content = f"""# SPDX-License-Identifier: PROPRIETARY
# Auto-generated by compliance-dimensions-init.py
# Do not edit manually - regenerate to update

id: {dimension['id']}
domain: {dimension['domain']}
name: {dimension['name']}
threshold: {dimension['threshold']}
enforcement: {dimension['enforcement']}
verification: {dimension['verification']}
tool: {dimension['tool']}
"""
    
    dimension_file.write_text(yaml_content)
    print(f"✅ Created: {dimension_file.relative_to(DOCS_ROOT)}")


def main():
    """Main entry point."""
    if not DOCS_ROOT.exists():
        print(f"❌ Documentation root not found: {DOCS_ROOT}")
        print("   Make sure saraise-documentation repository is available")
        sys.exit(1)
    
    print("📝 Initializing compliance dimensions...")
    print("=" * 60)
    
    for dimension in COMPLIANCE_DIMENSIONS:
        create_dimension_file(dimension)
    
    print(f"\n✅ Created {len(COMPLIANCE_DIMENSIONS)} compliance dimensions")
    print(f"   Dimensions stored in: {GCR_ROOT.relative_to(DOCS_ROOT)}/dimensions/")


if __name__ == "__main__":
    main()
